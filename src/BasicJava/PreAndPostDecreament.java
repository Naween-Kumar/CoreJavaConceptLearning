package BasicJava;

public class PreAndPostDecreament {

	public static void main(String[] args) {

		
		//Famous interview question ---> Post decrement 
		int a = 2;
		int b = a--; 
		
		System.out.println(a);  //>>1
		System.out.println(b);  //>>2
		
		
		//Famous interview question ---> Pre decrement 
				int x = 2;
				int y = --x; 
				
				System.out.println(x);  //>>1
				System.out.println(y);  //>>1
		
				
		
		
	}

}
