package BasicJava;
public class DataType {

	// main() method --> starting point of java program in a class
	// Project name, Package name and class name always start with first capital letter 
	// All the keywords start with small letter like class, public void main, static, int, double
	// String is a class not a keyword or data type
	// Primitive data type --> int, double, char, boolean
	public static void main(String[] args) {
	
		System.out.println("Hi How are you");
		
		//data types in java
		
		int i = 10;
		double d = 20.2;
		char c = 'a';
		boolean b = true;
		
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(i);
		System.out.println(d);
		

	}
}
