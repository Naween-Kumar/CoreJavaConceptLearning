package OOPSPart3;

public class EncapsulationConcept {
	
	
	private int cardnumber;
	private String ssn;
	
	
	
	public int getCardnumber() {
		return cardnumber;
	}
	public void setCardnumber(int cardnumber) {
		this.cardnumber = cardnumber;
	}
	public String getSsn() {
		return ssn;
	}
	public void setSsn(String ssn) {
		this.ssn = ssn;
	}
	
	

}
